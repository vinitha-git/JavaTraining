package Aggregation;

/**
 * class Product with data members: id, name, and description
 * @author VINITHA EDWIN
 */
public class Product {

	int id;
	String name;
	String description;
	/**
	 * @param id
	 * @param name
	 * @param description
	 */
	public Product(int id, String name, String description) {
		super();
		this.id = id;
		this.name = name;
		this.description = description;
	}
}
