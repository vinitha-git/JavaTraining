package Aggregation;

/**
 * Create a class Car with data members: color, carName, regNo
 * @author VINITHA EDWIN
 */
public class Car {
	
	String color;
	String carName;
	String regNo;
	/**
	 * @param color
	 * @param carName
	 * @param regNo
	 */
	public Car(String color, String carName, String regNo) {
		super();
		this.color = color;
		this.carName = carName;
		this.regNo = regNo;
	}

}
