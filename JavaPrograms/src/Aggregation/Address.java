package Aggregation;
/**
 * Get the address of student
 * @author VINITHA EDWIN
 * */
public class Address {
	
	String hName;
	String city;
	String country;
	int pincode;
	/**
	 * @param hName
	 * @param city
	 * @param country
	 * @param pincode
	 */
	public Address(String hName, String city, String country, int pincode) {
		super();
		this.hName = hName;
		this.city = city;
		this.country = country;
		this.pincode = pincode;
	}


}
